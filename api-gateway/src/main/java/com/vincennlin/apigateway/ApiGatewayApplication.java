@OpenAPIDefinition(
		info = @Info(
				title = "API Gateway",
				version = "1.0",
				description = "API Gateway for Flashcardweb"
		)

)
@SpringBootApplication
public class ApiGatewayApplication {

	public static void main(String[] args) {
		SpringApplication.run(ApiGatewayApplication.class, args);
	}

}
